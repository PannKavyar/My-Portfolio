//parseInt() Method

var HTMLBadges = prompt("How many HTML badges do you have?");
var CSSBadges = prompt("How many CSS badges do you have?");
var totalBadges = parseInt(HTMLBadges) + parseInt(CSSBadges);
alert("Wow! You have " + totalBadges + "badges!");

//parseFloat() Method

var killRate = prompt("How many people do you kill in a game?");
var footballRate = prompt("How many speed rate do you have in a match?");
var totalRate = parseFloat(killRate) + parseFloat(footballRate);
alert("Wow! You have " + totalRate + "rates!");